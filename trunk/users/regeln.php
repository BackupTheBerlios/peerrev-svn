												<p>Daneben ist ein kurzes schriftliche Gutachten anzufertigen, in der die Bewertung begr&uuml;ndet wird. Dabei sind m&ouml;glichst die im Praktikum erarbeiteten Fachtermini zu gebrauchen. </p>
												<p><a name="unlesbar"></a><b>Unlesbare Dokumente</b> bitte zun&auml;chst mit den Pool-Rechnern zu &ouml;ffnen versuchen. Sollte sich das Dokument hartn&auml;ckig widersetzen, bitte mit 0 Punkten bewerten und lediglich &raquo;Dokument nicht lesbar.&laquo; in das Gutachten schreiben. Ich sehe mir alle Dokumente an, die eine <i>0-Punkte-</i>Bewertung bekommen.</p>
												<p>Da fehlende Fonts von den Programmen i. d. R. erw&auml;hnt werden, bitte zun&auml;chst versuchen, einen Ersatzfont zu finden. Layoutprobleme wegen fehlender Fonts sollten nicht zu Punktabzug f&uuml;hren, im Gutachten jedoch erw&auml;hnt werden.</p>
												<p>Als Leiter der Veranstaltung halte ich mir vor, bei allzu grosser Diskrepanz der Gutachten strukturierend einzugreifen. Im Normalfall werden die Punkte der Gutachten aufsummiert und bilden die Gesamtpunktzahl f&uuml;r das Dokument. Weitere Hinweise und Beispiele zu Gutachten stehen auf einer <a href="regelnGutachten.php">eigenen Seite</a>.</p>
												<p>Achtung: Die Abgabe der  Gutachten ist Bedingung f&uuml;r die Validierung der eigenen Punkte. Keine Gutachten, keine Punkte. Damit bei fehlendem Gutachten der Autor nicht bestraft wird (sondern nur der s&auml;umige Gutachter), wird in diesem Fall mit Hilfe des Mittelwerts der vorhandenen Gutachten die Gesamtpunktzahl aufgestockt.</p>
												<h2>Hinweis an Alle:</h2>
												<p>Auf Grund der grossen Teilnehmerzahl kann ich auf individuell sicherlich gut begr&uuml;ndete Einzelw&uuml;nsche (Fristverl&auml;ngerung, ungerechte Bewertung etc.) nur wenig eingehen. </p>
												<h2><a name="mindestpunktzahl"></a>Mindestpunktzahl</h2>
												<p>F&uuml;r die Zulassung zur Pr&uuml;fung ist die Teilnahme am Praktikum sowie das Erreichen einer Mindestpunktzahl Voraussetzung.</p>
											<p>Das Praktikum beinhaltet <b>sieben Aufgaben</b>. Bei jeder &Uuml;bung ist ein Dokument einzureichen, welches durch <b>f&uuml;nf Gutachten</b> bewertet wird. F&uuml;r ein Gutachten (siehe Regeln f&uuml;r Gutachten) gibt es maximal <b>f&uuml;nf Punkte</b>. Somit k&ouml;nnen bis zu <b>25 Punkte pro Aufgabe</b> erreicht werden. Bei sieben Aufgaben ergibt das eine <b>Gesamtpunktzahl von 175 Punkten</b>.</p>

											<p><b>F&uuml;r die Zulassung zur Pr&uuml;fung m&uuml;ssen 50 Punkte erreicht werden.</b></p>

											<p>Gleiches gilt f&uuml;r die Ausstellung eines Teilnahmescheins f&uuml;r das Praktikum.</p>
											<h2><a name="abgabetermin"></a>Aktion gegen Nachtarbeit &ndash; Abgabetermin</h2>
										<p>Um zu verhindern, dass Aufgaben »auf den letzten Dr&uuml;cker« bearbeitet werden und am Abgabetag die Nacht durchgearbeitet wird, ist der offizielle Abgabetermin f&uuml;r die zu bearbeitende &uuml;bung ist jeweils 1 Wochen nach Aufgabenstellung um 0:00 Uhr morgens. Zus&auml;tzlich gibt es ein unbekanntes Zeitfenster in dem nach dem offiziellen Abgabetermin noch Einreichungen akzeptiert werden. Dieses Zeitfenster kann lediglich einige Minuten betragen. Daher sollte die Aufgabe sp&auml;testens zum Abgabetermin eingereicht werden.</p>

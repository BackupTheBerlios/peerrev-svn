			<h1>Spielregeln f&uuml;r das Praktikum und das Gutachterverfahren</h1>
			<p>Hinweis: F&uuml;r die Zulassung zur Pr&uuml;fung ist die Teilnahme am Praktikum und das Erreichen einer <a href= "#mindestpunktzahl">Mindestpunktzahl</a> erforderlich.</p>

												<p>Um die  grosse Teilnehmerzahl im Praktikum zu bew&auml;ltigen, werden wir die Beurteilung der Praktikumsbeitr&auml;ge durch das Gutachterverfahren gleichm&auml;&szlig;ig verteilen. Dieses Spiel hat folgende Regeln:</p>
												<h2>Einmalige Anmeldung zum Gutachterverfahren</h2>
												<p>Zur Teilnahme am Gutachterverfahren ist eine <a href="anmelden.php">einmalige Anmeldung</a> erforderlich. Dies geschieht zur Verwaltung der Matrikelnummern und der Emailadressen, an welche die Gutachterauftr&auml;ge verschickt werden.</p>
												<h2><b>Struktur einer &Uuml;bungsaufgabe</b></h2>
												<p>Jede Woche wird eine &Uuml;bungsaufgabe gestellt. Zu ihrer Bearbeitung ist wiederum eine Woche vorgesehen. In der Aufgabenstellung wird angegeben:</p>
												<ul>
													<li><a href="#abgabetermin">Abgabetermin</a></li>
													<li>Format des Dokuments (z.B. PDF, PSD, MOV o.&auml;.)</li>
													<li>Formale Anforderungen an die Gestaltungsleistung</li>
													<li>Inhaltliche Vorschl&auml;ge</li>
												</ul>
			<p>Die Aufgabe ist bis sp&auml;testens zum Abgabetermin vom <i>Autor</i> als <i>Dokument</i> einzureichen. Jedes Dokument wird in einem Gutachterverfahren von 3 Gutachtern bewertet (s.u.)</p>
			<h2>Hinweise f&uuml;r Autoren</h2>

												<p>Ziel der &Uuml;bungsaufgaben ist die Erarbeitung grundlegender Abläufe der Mediengestaltung. Jede Aufgabenstellung wird in dieser Hinsicht klar formuliert. Formal wird es also Vorgaben geben, die erf&uuml;llt werden m&uuml;ssen. Die Umsetzung dieser Vorgaben  wird im Praktikum erl&auml;utert.</p>
												<p>Die Inhalte der Aufgaben sollen m&ouml;glichst frei w&auml;hlbar sein, die Bearbeitung eigener Projekte ist damit ausdr&uuml;cklich erw&uuml;nscht. Doch auch hier werden einige Vorschl&auml;ge gemacht.</p>
												<p><b>Einreichen und Anmelden der Dokumente</b></p>
												<p>Dokumente sind zum vorgegebenen Termin <a href="dokument_einreichen.php">einzureichen. </a>Achtung: Ein Dokument wird verbindlich eingereicht, d.h. Korrekturen oder Verbesserungen sind danach nicht mehr m&ouml;glich. Das Lernziel ist dabei die Vorbereitung auf &auml;hnliche Situationen bei Bewerbungen und Projekten.</p>

												<p>Jeder Autor, der sein Dokument fristgerecht zur Begutachtung eingereicht hat, bekommt nach Ablauf der Anmeldefrist 3 andere Dokumente zur <a href="gutachten_abgeben.php">Begutachtung</a> zugeteilt. Jedes eingereichte und angemeldete Dokument wird von 3 Gutachtern beurteilt. Unterschiede zwischen einzelnen Gutachtern werden sich im Laufe des Semester heraus rechnen.</p>
